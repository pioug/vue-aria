import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const root = process.cwd();
const packagesRoot = path.join(root, "packages", "@vue-spectrum");
const stylePath = path.join(root, "docs", ".vitepress", "theme", "spectrum-base.css");
const reportPath = path.join(root, "docs", "porting", "style-alias-gaps.md");

const args = new Set(process.argv.slice(2));
const shouldWrite = args.has("--write");

const CLASS_TOKEN_PATTERN = /\b(?:spectrum|react-spectrum)-[A-Za-z0-9-]+\b/g;

function readFile(absolutePath) {
  return fs.readFileSync(absolutePath, "utf8");
}

function collectSourceFiles(directory) {
  if (!fs.existsSync(directory)) {
    return [];
  }

  const files = [];
  const stack = [directory];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) {
      continue;
    }

    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const next = path.join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(next);
        continue;
      }

      if (entry.isFile() && next.endsWith(".ts")) {
        files.push(next);
      }
    }
  }

  return files.sort();
}

function isLikelyRuntimeClassToken(token) {
  if (token.startsWith("react-spectrum-")) {
    return true;
  }

  if (token === "spectrum-provider" || token === "spectrum-theme") {
    return true;
  }

  if (token.startsWith("spectrum--")) {
    return true;
  }

  if (token.endsWith("-")) {
    return true;
  }

  return /spectrum-[A-Z]/.test(token);
}

if (!fs.existsSync(stylePath)) {
  throw new Error(`Missing docs style file: ${stylePath}`);
}

const styleContent = readFile(stylePath);
const sourceFiles = collectSourceFiles(packagesRoot);
const tokenSources = new Map();

for (const filePath of sourceFiles) {
  const content = readFile(filePath);
  for (const match of content.matchAll(CLASS_TOKEN_PATTERN)) {
    const token = match[0];
    if (!token) {
      continue;
    }

    const normalizedPath = path.relative(root, filePath);
    if (!tokenSources.has(token)) {
      tokenSources.set(token, new Set());
    }
    tokenSources.get(token).add(normalizedPath);
  }
}

const likelyTokens = [...tokenSources.keys()]
  .filter((token) => isLikelyRuntimeClassToken(token))
  .sort();
const missingTokens = likelyTokens.filter((token) => !styleContent.includes(token));

console.log("Spectrum docs style alias gap report:");
console.log(`- Runtime class-like tokens found: ${likelyTokens.length}`);
console.log(`- Missing alias/style selectors: ${missingTokens.length}`);

if (missingTokens.length > 0) {
  console.log("- Missing tokens:");
  for (const token of missingTokens) {
    const files = [...(tokenSources.get(token) ?? [])].slice(0, 3).join(", ");
    console.log(`  - ${token} (${files})`);
  }
}

if (shouldWrite) {
  const now = new Date();
  const isoDate = `${now.getUTCFullYear()}-${String(now.getUTCMonth() + 1).padStart(2, "0")}-${String(
    now.getUTCDate()
  ).padStart(2, "0")}`;

  const lines = [];
  lines.push("# Spectrum Docs Style Alias Gaps");
  lines.push("");
  lines.push("Auto-generated by `scripts/mine-spectrum-style-alias-gaps.mjs --write`.");
  lines.push("");
  lines.push(`- Generated (UTC): \`${isoDate}\``);
  lines.push(`- Runtime class-like tokens found: \`${likelyTokens.length}\``);
  lines.push(`- Missing alias/style selectors: \`${missingTokens.length}\``);
  lines.push("");

  if (missingTokens.length === 0) {
    lines.push("All detected runtime class-like tokens currently have selector coverage in:");
    lines.push("");
    lines.push("- `docs/.vitepress/theme/spectrum-base.css`");
    lines.push("");
  } else {
    lines.push("## Missing Tokens");
    lines.push("");
    for (const token of missingTokens) {
      const files = [...(tokenSources.get(token) ?? [])].slice(0, 5);
      lines.push(`- \`${token}\``);
      for (const filePath of files) {
        lines.push(`  - \`${filePath}\``);
      }
    }
    lines.push("");
  }

  fs.writeFileSync(reportPath, lines.join("\n"));
  console.log(`Wrote ${path.relative(root, reportPath)}`);
}
